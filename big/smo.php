        <h2 class="subheader">Social Media Optimization</h2>
        <p>Search engine optimization used to be the only way that a website gets recognized and utilized by its targeted audience. Nowadays, optimizing social media platforms also determines a business's success. This article will highlight to readers what social media optimization is and why it is crucial in reaching out to consumers to offer services.</p>

        <p>Before discussing the reasons social media optimization is important, we must first understand what social media optimization is. Investopedia states: “Social media optimization (SMO) is the use of social media networks to manage and grow an organization's message and online presence” (Kenton, 2021). Social media optimization means using social media tools to advertise a business to its targeted audience. Out of all social media tools, the most popular ones in America are Youtube and Facebook (Pews Research Center, 2021).</p>

        <p>SMO is a powerful tool for reaching out to a wide range of users. This is because a majority of people use social media. According to Pew Research Center, today “72% of the public uses some type of social media” in America (Social Media Fact Sheet, 2021). This means by engaging with social media, a business gains presence to millions of users around the country. Being recognized by users can give a business's brand a boost of popularity that it needs.</p>

        <p>Social media optimization also works hand-in-hand with search engines in order to boost a business's image. According to LinkedIn, this is because “search engines have started acquiring data from social media channels” (Deshpande, 2015). Search engines are using social media in order to determine whether certain words, images, or brands are popular. The more engaging a brand's social media is, the more search engines recommend the brand. This further increases the amount of people that are interested in a business's services.
  
        <p>In short, social media are powerful tools to advertise a service. Businesses should optimize social media into their model so that users are able to recognize them and use their services.</p>

