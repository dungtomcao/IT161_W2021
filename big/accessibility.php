        <h2 class="subheader">Accessibility Research</h2>

        <p>There are many steps to making a website both intuitive and aesthetically pleasing in order to attract viewers. One of those steps is making one's website accessible for people with disabilities. This essay will outline how providing accessibility for people with disability is integral for a website to connect and provide services for as many individuals as possible.</p>

        <div class='embed-container'><iframe width='560' height='315' src='https://www.youtube.com/embed/hufMi9LZX2I' title='YouTube video player' frameborder='0' allow='accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture' allowfullscreen></iframe></div>

        <p>First, we must not forget to mention that there is a large community of disabled users who utilize technology to get services they need. In fact, according to the CDC, “61 million adults in the United States live with a disability,” which means 26% of the U.S. population (Disability Impacts All of Us). This demonstrates a significant number of people that websites which do not prioritize accessibility fail to reach out. Designs that focus on disabled users will be able to reach a wider audience in order to provide a website's services.</p>

        <p>Failing to provide accessibility on one's website means facing financial loss not only because a website fails to reach its audiences, but also because of the law. Countries of the United Nations follow the Convention on the Rights of Person with Disabilities, which includes “identification and elimination of obstacles and barriers to accessibility” that apply to “information, communications and other services, including electronic services and emergency services.” (Article 9 – Accessibility | United Nations Enable). Similar rules are applied by the Americans with Disabilities Act. In fact, businesses “must ensure that the programs, services, and goods that they provide to the public—including those provided online—are accessible to people with disabilities.” (Guidance on Web Accessibility and the ADA, 2022). This means a website will not be able to provide services if it refuses to make them available and accessible to people with disabilities. Businesses will face major losses if their websites are inaccessible to disabled people.</p>

        <p>Lastly, Accessible websites increase overall users' engagement and for people to find what they need efficiently. A comprehensible text-to-speech option on a website not only helps the visually impaired, but also people who prefer their information read to them. Search bars with spell checkers not only help people with dyslexia find information they need, but also others who make typos on small keyboards on a phone. Accessible websites are beneficial to not just people who need them but also everybody else.</p>

        <p>In a nutshell, making a website accessible is important to reaching out to a wide range of audience. It is required by law, and can only benefit everyone. Therefore, businesses should make their websites as accessible as possible.</p>

